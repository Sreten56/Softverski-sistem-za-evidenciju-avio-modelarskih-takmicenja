    private void jButtonDODAJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDODAJActionPerformed

        int IDamk = vrati_id_amk() + 1;

        String naziv = jTextFieldNAZIV.getText();
        String tel = jTextFieldTLEFON.getText();
        String email = jTextFieldEMAIL.getText();
        
        if (jComboBoxMESTA.getSelectedIndex() == -1 || jComboBoxMESTA.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(this,
                        "Morate odabrati opciju iz padajućeg menija!",
                        "Greška",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

        //System.out.println("MESTO: " + mestoID);
        AMKlub amk = new AMKlub(IDamk, naziv, tel, email, mestoID);

        Klijentski_Zahtev kz = new Klijentski_Zahtev(Operacije.DODAJ_AMK, amk);
        Komunikacija.getInstance().posalji_zahtev(kz);
        Serverski_Odgovor so = Komunikacija.getInstance().primi_odgovor();

        if (so == null || so.getOdgovor().equals("NEUSPEH!")) {

            JOptionPane.showMessageDialog(this, "Greska prilikom dodavanja AM kluba u bazu!\n"
                    + "Proverite da li ste upisali vrednosti u sva polja i odabrali mesto iz padajuceg menija \n"
                    + "Takodje proverite konekciju sa BAZOM!", "GRESKA!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JOptionPane.showMessageDialog(this, "Uspesno dodavanje AM kluba u bazu!", "USPEH!", JOptionPane.INFORMATION_MESSAGE);
        famk.PRIKAZI_PODATKE_TABELE();
        this.dispose();

    }//GEN-LAST:event_jButtonDODAJActionPerformed

