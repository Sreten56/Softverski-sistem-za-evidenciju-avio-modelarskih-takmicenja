    private void popuniCB_mesto() {

        // 
        Klijentski_Zahtev kzM = new Klijentski_Zahtev(Operacije.VRATI_SVA_MESTA, null);
        Komunikacija.getInstance().posalji_zahtev(kzM);
        Serverski_Odgovor soM = Komunikacija.getInstance().primi_odgovor();

        listaMesta = (List<Mesto>) soM.getOdgovor();
        jComboBoxMESTA.removeAllItems();

        //
        for (Mesto mes : listaMesta) {
            jComboBoxMESTA.addItem(mes.getNazivMesto() + " (" + mes.getOpstina() + ")");
        }
        jComboBoxMESTA.setSelectedIndex(-1);

        // 4. Listener za promene tj "lajv apdejt"
        jComboBoxMESTA.addActionListener(e -> {
            int index = jComboBoxMESTA.getSelectedIndex();
            if (index >= 0 && index < listaMesta.size()) {
                mestoID = listaMesta.get(index).getIdMesto();
                System.out.println("Izabrano mestoID: " + mestoID);
            }
        });
    }

    private void postaviPocetnuVrednostCB() {
        if (amk != null && listaMesta != null) {
            for (int i = 0; i < listaMesta.size(); i++) {
                Mesto mes = listaMesta.get(i);
                if (amk.getMestoID() == mes.getIdMesto()) {
                    jComboBoxMESTA.setSelectedIndex(i); // postavi selektovani index
                    mestoID = mes.getIdMesto();
                    return;
                }
            }
        }
    }

