    private void azurirajPodatkeCB_mesto() {

        if (listaMesta == null || listaMesta.isEmpty()) {
            return;
        }

        String izabranoMesto = (String) jComboBoxMESTA.getSelectedItem();
        if (izabranoMesto == null) {
            return;
        }

        for (Mesto mesto : listaMesta) {
            if (mesto.getNazivMesto().equals(izabranoMesto)) {
                mestoID = mesto.getIdMesto();
                return;
            }
        }

    }

    private int vrati_id_amk() {

        Klijentski_Zahtev kz = new Klijentski_Zahtev(Operacije.VRATI_NAJVECI_ID_AMK, null);
        Komunikacija.getInstance().posalji_zahtev(kz);
        Serverski_Odgovor so = Komunikacija.getInstance().primi_odgovor();

        int najveciId = 0;
        if (so != null && so.getOdgovor() instanceof Integer) {
            najveciId = (Integer) so.getOdgovor();
        }
        //System.out.println("Najveći ID amk: " + najveciId);
        return najveciId;
    }
